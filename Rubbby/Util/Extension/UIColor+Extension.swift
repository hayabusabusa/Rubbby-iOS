import UIKit

extension UIColor {
    class var primary: UIColor {
        return UIColor(named: "Primary")!
    }
    class var navigation: UIColor {
        return UIColor(named: "Navigation")!
    }
    class var containerBackground: UIColor {
        return UIColor(named: "ContainerBackground")!
    }
}
